
public class ShiftOperator {
	
	public static void main(String args[]){  
	  
	
		System.out.println(20>>2);  
	    
		System.out.println(40>>>2);  
	    
	    
	    //For negative number, >>> changes parity bit (MSB) to 0  
	    
	    System.out.println(-10>>2);  
	    
	    System.out.println(-20>>>2);  
	}
}
