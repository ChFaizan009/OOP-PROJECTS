package com.uog.project.bucket;

public class Bucket {

	int data;
	Bucket next;
	
	///Getter and Setter 
	public int getData() {
		return data;
	}
	public void setData(int data) {
		this.data = data;
	}
	public Bucket getNext() {
		return next;
	}
	public void setNext(Bucket next) {
		this.next = next;
	}
	
}
